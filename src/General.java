class General {
    private String name;
    private int yearsOfService;

    public General(String name, int yearsOfService) {
        this.name = name;
        this.yearsOfService = yearsOfService;
    }

    public String getName() {
        return name;
    }

    public int getYearsOfService() {
        return yearsOfService;
    }
}
